function createDemoVideoAnalysis(
  analyzedAt: string,
  input: {
    provider?: "openai-vision" | "smartplay-fallback";
    summary: string;
    playerSnapshot: string;
    strengths: string[];
    improvementAreas: string[];
    coachingPoints: string[];
    nextSessionFocus: string;
    mindsetCue: string;
    detectedTags: string[];
    moments: Array<{
      timestamp: string;
      title: string;
      observation: string;
      coachingCue: string;
      tags: string[];
    }>;
    recommendedDrills: Array<{
      title: string;
      purpose: string;
      reps: string;
      equipment: string;
    }>;
    metadata?: {
      fileType?: "video" | "image";
      sampledFrames?: number;
      durationSeconds?: number | null;
      width?: number | null;
      height?: number | null;
      fps?: number | null;
    };
    confidence?: "high" | "medium" | "low";
  },
) {
  return {
    provider: input.provider ?? "smartplay-fallback",
    analyzedAt,
    summary: input.summary,
    playerSnapshot: input.playerSnapshot,
    strengths: input.strengths,
    improvementAreas: input.improvementAreas,
    coachingPoints: input.coachingPoints,
    recommendedDrills: input.recommendedDrills,
    mindsetCue: input.mindsetCue,
    nextSessionFocus: input.nextSessionFocus,
    confidence: input.confidence ?? "medium",
    detectedTags: input.detectedTags,
    moments: input.moments,
    metadata: {
      fileType: input.metadata?.fileType ?? "image",
      sampledFrames: input.metadata?.sampledFrames ?? 1,
      durationSeconds: input.metadata?.durationSeconds ?? null,
      width: input.metadata?.width ?? 1280,
      height: input.metadata?.height ?? 720,
      fps: input.metadata?.fps ?? null,
    },
  };
}

